# function that takes 2 parameters, age and name
def show_young_age(my_age, my_name):
    print("My age 10 years ago was {0}.\n"
          "My name is {1}."
          .format(my_age - 10, my_name))
 
# invoking function
show_young_age(22, "Tuheitia")
 
